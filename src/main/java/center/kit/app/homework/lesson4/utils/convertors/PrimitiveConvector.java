package center.kit.app.homework.lesson4.utils.convertors;

public class PrimitiveConvector {

    //----------    Float -> Char CONVERTOR ---------------
    public static  char float2char (float inputFloatVariable){
        return (char)inputFloatVariable;
    }
    //----------    Int -> Char CONVERTOR ---------------
    public static  char int2char (int inputIntVariable){
        return (char)inputIntVariable;
    }
    //----------    Char -> Int CONVERTOR ---------------
    public static  int char2int (char inputCharVariable){
        return (int)inputCharVariable;
    }

}
